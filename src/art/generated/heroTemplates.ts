export function roleToTemplate(role: string, tags: string[] = []): HeroTemplateKey {
  const t = tags.map(t => t.toLowerCase())
  if (t.includes('slime') || t.includes('golem') || t.includes('blob')) return 'blob'
  if (t.includes('reaper') || t.includes('cat') || t.includes('mythic')) return 'reaper'
  switch (role) {
    case 'tank':     return 'tank'
    case 'healer':   return 'healer'
    case 'ranged':   return 'ranged'
    case 'caster':   return 'caster'
    case 'assassin': return 'assassin'
    case 'support':  return 'support'
    default:         return 'tank'
  }
}
